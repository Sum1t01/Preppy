fun themePreviewColor(theme: ThemePalette): Color {
    return when (theme) {

        ThemePalette.DEFAULT_GREEN -> Color(0xFF3EC300)
        ThemePalette.OCEAN_BLUE -> Color(0xFF0077FF)
        ThemePalette.SUNSET_ORANGE -> Color(0xFFFF4D2E)
        ThemePalette.MONOCHROME -> Color(0xFF111111)

        ThemePalette.FOREST -> Color(0xFF2E7D32)
        ThemePalette.NEON_PURPLE -> Color(0xFF7C4DFF)
        ThemePalette.COFFEE -> Color(0xFF6F4E37)
        ThemePalette.MIDNIGHT -> Color(0xFF1E3A8A)
    }
}